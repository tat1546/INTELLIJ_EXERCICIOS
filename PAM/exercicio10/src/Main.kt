package fundamentos.controles

fun main(args: Array<String>) {
    // Declaração de uma variável imutável (val) 'nota' do tipo Double, inicializada com o valor 6.9
    val nota: Double = 6.9
    // Estrutura condicional 'if-else' que verifica se a 'nota' é maior ou igual a 7.0
    if (nota >= 7.0){
        // Se a condição for verdadeira, imprime a mensagem "Aprovado!!"
        println("Aprovado!!")
    }else {
        // Se a condição for falsa, imprime a mensagem "REPROVADO !!"
        println("REPROVADO !!")
    }
}