package fundamentos.controles
fun main(args: Array<String>) {
    // Declaração e inicialização de uma variável mutável (var) 'contador' com o valor 1
    var contador : Int = 1
    // Estrutura de repetição 'while' que executa enquanto 'contador' for menor ou igual a 10
    while (contador <= 10) {
        // Imprime o valor atual de 'contador'
        println(contador)
        // Incrementa 'contador' em 1
        contador++
    }
}