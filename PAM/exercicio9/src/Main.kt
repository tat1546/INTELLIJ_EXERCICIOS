package fundamentos.controles
fun main(args: Array<String>) {
    // Declaração de uma variável imutável (val) 'nota' do tipo Double, inicializada com o valor 8.3
    val nota : Double =8.3
    // Estrutura condicional 'if' que verifica se a 'nota' é maior ou igual a 7.0
    if(nota >= 7.0){
        // Se a condição for verdadeira, imprime a mensagem "Aprovados"
        println("Aprovados")
    }
}