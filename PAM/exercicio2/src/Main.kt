package fundamentos

fun main() {
    // Declaração de uma variável imutável (val) chamada 'name' contendo a string "Kotlin"
    val name = "Kotlin"
    // Imprime uma mensagem usando a concatenação de strings
    println("Hello," + name + "!")
    // Estrutura de repetição 'for' que itera de 1 até 5
    for(i in 1 .. 5 ){
        // Imprime o valor da variável 'i'
        println("i = $i")
    }
}